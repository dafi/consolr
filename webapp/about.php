        <h1>Consolr - How to easily manage scheduled tumblr</h1>
        <h2>
        <a href="http://consolr.netsons.org/">Consolr</a> is a administration console for <a href="http://www.tumblr.com/">Tumblr</a> blogs, it makes easy to manage scheduled posts for publication
        </h2>
        <h2>Consolr can</h2>
        <ul>
            <li>show the posts grouped by day</li>
            <li>rearrange the posts moving them using drag &amp; drop</li>
            <li>edit the posts double clicking on photo</li>
            <li>delete posts without use the dashboard</li>
            <li>show the used tags chart and their frequency</li>
            <li>filter visualization by tags</li>
            <li>check 'holes', i.e. days without posts</li>
            <li>upload to queue multiple images at once simply passing the photos URL</li>
            <li>show all published posts grouped by day</li>
        </ul>

        <p>Currently it is possible to manage only photo posts but soon will be possible to work on all types of tumblr (Text, Photo, Quote, Link, Audio, Video)</p>

